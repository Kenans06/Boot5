package com.example.gebruiker.sportapp.Homepage.Instellingen;

/**
 * @author BOOT-05
 *         User preferences model
 */
public class sUserPreferences {

    public boolean eten, muziek, sporten, gaming, dieren, schrijven, winkelen, familie, vrienden;

    public sUserPreferences(boolean eten, boolean muziek, boolean sporten, boolean gaming, boolean dieren, boolean schrijven, boolean winkelen, boolean familie, boolean vrienden) {
        this.eten = eten;
        this.muziek = muziek;
        this.sporten = sporten;
        this.gaming = gaming;
        this.dieren = dieren;
        this.schrijven = schrijven;
        this.winkelen = winkelen;
        this.familie = familie;
        this.vrienden = vrienden;
    }


}
